    private void sales(){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        
        int subTotal = Integer.parseInt(posSubTotal.getText());
        int subPay = Integer.parseInt(posPay.getText());
        int subBal = Integer.parseInt(posBalance.getText());
        int lastInsertId = 0;
        String cashier = labelName.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/superpos","root","");
            String query = "INSERT INTO sales(date,subtotal,pay,balance,cashier)VALUES(?,?,?,?,?) ";
            insert = con1.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);         //RETURN_GENERATED_KEYS untuk mendapatkan id nya ketika diinsert
            insert.setString(1, date);
            insert.setInt(2, subTotal);
            insert.setInt(3, subPay);
            insert.setInt(4, subBal);
            insert.setString(5, cashier);
            insert.executeUpdate();
            
            ResultSet generatedKeyResult = insert.getGeneratedKeys();                     //.getGeneratedKeys() ini ambil idnya
            
            if(generatedKeyResult.next()){
                lastInsertId = generatedKeyResult.getInt(1);
            }
            
            //JOptionPane.showMessageDialog(this, lastInsertId);                           // showMessage to debug ID
            
            int rows = tableList.getRowCount();
            
            String query1 = "INSERT INTO sales_product(sales_id,barcode,sell_price,qty,total) VALUES(?,?,?,?,?)";
            insert = con1.prepareStatement(query1);
            
            String barcode = "";
            int price = 0;
            int qty = 0;
            int total = 0;
        
            for(int i = 0; i<tableList.getRowCount(); i++){
                barcode = tableList.getValueAt(i,0).toString();
                price = Integer.parseInt(tableList.getValueAt(i,2).toString());
                qty = Integer.parseInt(tableList.getValueAt(i,3).toString());
                total = Integer.parseInt(tableList.getValueAt(i,4).toString());
              
                insert.setInt(1,lastInsertId);
                insert.setString(2,barcode);
                insert.setInt(3,price);
                insert.setInt(4,qty);
                insert.setInt(5,total);
                insert.executeUpdate();
            }
         
            
            
            String query2 = "UPDATE product SET qty=qty-? WHERE barcode=?";
            insert = con1.prepareStatement(query2);
            
            for(int i=0; i<tableList.getRowCount(); i++){
                barcode = (String)tableList.getValueAt(i,0).toString();
                qty = Integer.parseInt(tableList.getValueAt(i,3).toString());
                insert.setInt(1,qty);
                insert.setString(2,barcode);
                insert.execute();
                
            }
            
            insert.addBatch();
         
            JOptionPane.showMessageDialog(this, "Record Saved");
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    
