    private void posInsert(){
        String barcode = productBarcode.getText();
        
        if(productQty.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Qty is required!");
            return;
        }
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/superpos","root","");

            insert = con1.prepareStatement("SELECT * FROM product WHERE barcode=?");
            insert.setString(1,barcode);
            rs = insert.executeQuery();
            
            while(rs.next()){
                
                int qtyCurrent;
                
                qtyCurrent = rs.getInt("qty");

                int price = Integer.parseInt(productPrice.getText());
                int qtyInput = Integer.parseInt(productQty.getText());
                
                int total = price * qtyInput;
                
                if(qtyInput>qtyCurrent){
                    JOptionPane.showMessageDialog(this, "Qty is not enough!\nAvailable Product = " + qtyCurrent);
                }else{
                    model = (DefaultTableModel)tableList.getModel();
                    model.addRow(new Object[]{
                        productBarcode.getText(),
                        productName.getText(),
                        productPrice.getText(),
                        productQty.getText(),
                        total
                    });
                    
                    // count all
                    int sum = 0;
                    for(int i=0; i<tableList.getRowCount(); i++){
                        sum = sum + Integer.parseInt(tableList.getValueAt(i, 4).toString()); //4 is position total in table
                    }
                    
                    posSubTotal.setText(Integer.toString(sum));
                    
                    // reset
                    productBarcode.setText("");
                    productName.setText("");
                    productPrice.setText("");
                    productQty.setText("");
                }
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }
    
    private void setDataForm(){
        String barcode = productBarcode.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/superpos","root","");

            insert = con1.prepareStatement("SELECT * FROM product WHERE barcode=?");
            insert.setString(1,barcode);
            rs = insert.executeQuery();

            if(rs.next()==false){
                JOptionPane.showMessageDialog(this,"Barcode Not Found!");
            }else{
                String productNameData = rs.getString("product");
                String productPriceData = rs.getString("retail_price");

                productName.setText(productNameData.trim());
                productPrice.setText(productPriceData.trim());
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
